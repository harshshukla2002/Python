# if statement
if 1 < 2:
    print("1 is smaller than 2")

# if else statement
if 3 < 2:
    print("3 is smaller than 2")
else:
    print("3 is grater than 2")

# elif statement
if 3 > 4:
    print("3 is greater than 4")
elif 3 < 4:
    print("3 is smaller than 4")
else:
    print("3 is equal to 4")
