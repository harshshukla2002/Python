myTuple = (1, 2, 3)

# to get length
print(f"length is {len(myTuple)}")

# to get value using index
print(f"value at index 1 is {myTuple[1]}")

# to get count how many times a element in appears in tuple
newTuple = (1, 2, 3, 4, 4, 4)
print(f"4 {newTuple.count(4)}times present in tuple")

# to get index of an element
print(f"2 is present in index {newTuple.index(2)}")
